    fn periodic_y_evals_match() {
        let n = GENERATOR_POINTS_Y_COEFFS.len();
        let domain = Radix2EvaluationDomain::<Fp>::new(n).unwrap();
        let expected_evals = domain.fft(&GENERATOR_POINTS_Y_COEFFS);

        // easier to see differences with evals vs coefficients
        let (_, x_evals) = generator_points_evals();

        for (i, (expected, actual)) in zip(expected_evals, x_evals).enumerate() {
            assert_eq!(expected, actual, "mismatch at {i}");
        }
    }

    /// Ouptut is of the form (x_points_coeffs, y_points_coeffs)
    pub fn generator_points_evals() -> (Vec<Fp>, Vec<Fp>) {
        let mut evals = Vec::new();
        let mut acc = Projective::from(StarkwareCurve::GENERATOR);
        for _ in 0..251 {
            let p = acc.into_affine();
            evals.push((p.x, p.y));
            acc.double_in_place();
        }
        evals.resize(256, *evals.last().unwrap());
        evals.into_iter().unzip()
    }
